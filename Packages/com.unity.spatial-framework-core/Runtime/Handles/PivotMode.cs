namespace Unity.SpatialFramework.Handles
{
    /// <summary>
    ///   <para> Where the tool handle or transform manipulator is positioned.</para>
    /// </summary>
    public enum PivotMode
    {
        /// <summary>
        ///   <para>The tool handle or transform manipulator is at the center of the selection.</para>
        /// </summary>
        Center,
        /// <summary>
        ///   <para>The tool handle or transform manipulator is on the pivot point of the active object.</para>
        /// </summary>
        Pivot,
    }
}
